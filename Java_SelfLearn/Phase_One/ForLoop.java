public class ForLoop {
    public static void main(String[] args) {
        //////////////////////////////////////////// solution for loop
        for(int i = 0; i < 10; i++){
            System.out.print(i);
            System.out.println(" Hello");
        }
        //////////////////////////////////////////// solution for loop
        for(int i = 0; i < 10; i++){
            if(i == 5) break;
            System.out.print(i);
            System.out.println(" Hello");
        }
    }
}
// i++ is the same as i = i + 1 
// i-- is the same as i = i - 1
// i += 2 is the same as i = i + 2
// i -= 2 is the same as i = i - 2
// i *= 2 is the same as i = i * 2
// i /= 2 is the same as i = i / 2
